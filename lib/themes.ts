export function formatThemeCss(theme: ThemePreset): string {
  const formatBlock = (vars: Record<string, string>) =>
    Object.entries(vars)
      .map(([key, value]) => `  ${key}: ${value};`)
      .join("\n")

  const lightBlock = formatBlock(theme.light)
  const darkBlock = formatBlock(theme.dark)

  const parts: string[] = []

  if (lightBlock) {
    parts.push(`:root {\n${lightBlock}\n}`)
  }

  if (darkBlock) {
    parts.push(`.dark {\n${darkBlock}\n}`)
  }

  if (parts.length === 0) {
    return "/* Default theme — no overrides */"
  }

  return parts.join("\n\n")
}
